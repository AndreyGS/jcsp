    public static ICspSerializationProcessor createAutoGeneratedSerializationProcessor(Class<?> serializingClass)
        throws CspRuntimeException
    {
        if (!isAutoGeneratable(serializingClass))
        {
            throw CspRuntimeException.createCspRuntimeException(CspStatus.NO_SUCH_HANDLER,
                serializingClass.getName() + " is not annotated with " + CspProcessorAutoGeneratable.cl
                                                                             .ass.getName(),
                null);
        }

        List<ICspSerializationProcessor> callbacks = new ArrayList<ICspSerializationProcessor>();

        return new AutoGeneratedSerializationProcessor(serializingClass, callbacks);
    }

    private static boolean isAutoGeneratable(Class<?> clazz)
    {
        return clazz.isAnnotationPresent(CspProcessorAutoGeneratable.class);
    }

    private static void produceCallbacks(List<ICspSerializationProcessor> callbacks, Class<?> clazz)
    {
        Class<?> parentClazz = clazz.getSuperclass();
        if (isAutoGeneratable(parentClazz))
        {
            callbacks.add((value, context) -> context.getCspGeneralSerializationProcessor().serialize(value, parentClazz, context));
        }

        Arrays.stream(clazz.getDeclaredFields()).forEach(field -> field.setAccessible(true));

        Arrays.stream(clazz.getDeclaredFields())
            .filter(field -> field.getAnnotation(CspSerializable.class) != null)
            .sorted(Comparator.comparingInt(field -> field.getAnnotation(CspSerializable.class).order()))
            .forEach(field -> produceFieldSerializationCallbacks(callbacks, field));
    }

