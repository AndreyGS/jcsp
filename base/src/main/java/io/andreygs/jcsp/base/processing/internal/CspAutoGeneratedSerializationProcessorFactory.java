    private static class AutoGeneratedSerializationProcessor
        implements ICspSerializationProcessor
    {
        private final Class<?> clazz;
        private final List<ICspSerializationProcessor> callbacks;

        public AutoGeneratedSerializationProcessor(Class<?> clazz, List<ICspSerializationProcessor> callbacks)
        {
            this.clazz = clazz;
            this.callbacks = callbacks;
        }

        @Override
        public void serialize(Object value, ICspDataMessageSerializationContext context)
        {
            callbacks.forEach(callback -> callback.serialize(value, context));
        }

        private ICspSerializationProcessor getP(Field field, Object value, ICspDataMessageSerializationContext context)
        {
            ICspSerializationProcessor processor = (valueX, contextX) ->
                                                        context.getCspGeneralSerializationProcessor().serialize(field.getInt(valueX), contextX);
            return processor;
        }
    }
